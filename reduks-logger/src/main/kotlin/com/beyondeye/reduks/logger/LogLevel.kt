package com.beyondeye.reduks.logger

/**
 * standard android log levels translated to integer constants in order to pass log level as parameter
 * Created by daely on 7/25/2016.
 */
object LogLevel {
    const val v = 0
    const val VERBOSE = v
    const val d = 1
    const val DEBUG = d
    const val i = 2
    const val INFO = i
    const val w = 3
    const val WARN = w
    const val e = 4
    const val ERROR = e
    const val wtf = 5
    const val ASSERT = wtf
}